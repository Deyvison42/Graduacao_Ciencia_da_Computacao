#include <stdio.h>

int main() {

    // ================================
    // 1. OPERADORES RELACIONAIS
    // ================================
    int a = 10;
    int b = 20;

    printf("Valores: a = %d, b = %d\n", a, b);

    // Igualdade
    printf("a == b: %d\n", a == b); // 0 (falso)

    // Desigualdade
    printf("a != b: %d\n", a != b); // 1 (verdadeiro)

    // Maior que
    printf("a > b: %d\n", a > b); // 0 (falso)

    // Menor que
    printf("a < b: %d\n", a < b); // 1 (verdadeiro)

    // Maior ou igual a
    printf("a >= b: %d\n", a >= b); //

    printf("a <= b: %d\n", a <= b); // 1 (verdadeiro)

    return 0;
}